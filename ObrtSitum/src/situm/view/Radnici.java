    private void ucitajSlikuRadnika(String putanja) {
  try {
           Image i = ImageIO.read(new File(putanja));
   // Image i = ImageIO.read(new URL("https://result.issf-sports.info/get_image.php?issfid=SHIRLM2710197001&width=1500"));
        lblSlika.setIcon(new ImageIcon(i.getScaledInstance(100, 150, Image.SCALE_DEFAULT)));
        
        } catch (Exception e) {
        }    
    }

    private void ocistPolja() {
        txtOsnovica.setText("");
        txtIme.setText("");
        txtPrezime.setText("");
        txtMobitel.setText("");
        txtIBAN.setText("");
    }

    private void preuzmiVrijednosti(Radnik entitet) {
           entitet.setIme(txtIme.getText());
           entitet.setPrezime(txtPrezime.getText());
           entitet.setMobitel(txtMobitel.getText());
           entitet.setIban(txtIBAN.getText());
           entitet.setOsnovicaPoSatu(txtOsnovica.getText());

    }

    private void ucitajEntitete() {

         DefaultListModel<Radnik> m = new DefaultListModel<>();
        for(Radnik r : obradaEntitet.getLista()){
            m.addElement(r);
        }
        lstEntiteti.setModel(m);
        
        if(m.getSize()>0){
            lstEntiteti.setSelectedIndex(m.getSize()-1);
        }
    }

        
